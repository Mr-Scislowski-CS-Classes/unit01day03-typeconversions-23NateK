class DoubleOps {
    public static void main(String[] args) {


        System.out.println(Math.abs(Integer.parseInt(args[0])));

        System.out.println(Math.abs(Double.parseDouble(args[1])));

        System.out.println(Math.pow(Double.parseDouble(args[2]),Double.parseDouble(args[3])));
        
        System.out.println(Math.sqrt(Double.parseDouble(args[4])));


        System.out.println(Math.random());

    }
}